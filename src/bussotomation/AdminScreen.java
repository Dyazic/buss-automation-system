    //------------------------- driver bringer and driver search-------------------------------------
    public void bringDriver(){
        tableDriverModel.setRowCount(0);
        ArrayList<Drivers> drivers=new ArrayList<Drivers>();
        drivers=sql.driverCome();
        if(drivers!=null){
            for(Drivers driver:drivers){
                Object[] addRow={driver.getDriverNo(),driver.getDriverName(),driver.getDriverAge(),driver.getDriverTel()};
            tableDriverModel.addRow(addRow);
            }
         }
    }
    public void dynamicFindDriver(String find){
         TableRowSorter<DefaultTableModel>tf=new TableRowSorter<>(tableDriverModel);
      tableDriver.setRowSorter(tf);
      tf.setRowFilter(RowFilter.regexFilter(find));
        
    }
