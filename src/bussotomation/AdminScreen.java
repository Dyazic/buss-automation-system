    public void comboBoxDriverCall(){
        comboDriver.removeAllItems();
        ArrayList<Drivers>drivers=new ArrayList<Drivers>();
        drivers=sql.driverCome();
        if(drivers!=null){
            for(Drivers driver:drivers){
               // Object []addCombo={driver.getDriverName()};
               
                comboDriver.addItem(driver.getDriverName());
            }
        }
    }
    public void bringBusses(){
        tableBusModel.setRowCount(0);
        ArrayList<Busses>busses=new ArrayList<Busses>();
        busses=sql.bringBusses();
        if(busses!=null){
            for(Busses bus:busses){
               Object []add={bus.getBussNo(),bus.getBussPlate(),bus.getBussModel(),bus.getDriverName()}; 
               
               tableBusModel.addRow(add);
            }
        }
    }
    //----------------EXPEDİTİON SCREEN-------------------------
    public void bringExpedition(){
        tableExpeditionsModel.setRowCount(0);
        ArrayList<Expeditions>expeditions=new ArrayList<Expeditions>();
        expeditions=sql.bringExpeditions();
        if(expeditions!=null){
            for(Expeditions exped:expeditions){
                Object []add={exped.getExNo(),exped.getDeparture(),exped.getArrival(),exped.getDate(),exped.getHour(),exped.getBussNo(),exped.getTime(),exped.getPrice(),exped.getBussPlate(),exped.getBussDriver()};
                tableExpeditionsModel.addRow(add);
            }
        }
    }
